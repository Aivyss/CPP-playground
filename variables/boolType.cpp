#include <iostream>

using namespace std;

int boolType() {
    /**
     * bool: 0 or 1
     */

    bool a = 25;
    bool b = -1;
    bool c = 0;
    bool d = 1;
    cout << "(> 1) a = " << a << endl;
    cout << "(< 0) b = " << b << endl;
    cout << "(== 0) c = " << c << endl;
    cout << "(== 1) d = " << d << endl;

    bool e = true;
    bool f = false;
    cout << "(true) e = " << e << endl;
    cout << "(false) f = " << f << endl;

    return 0;
}